def _render_api_test_case(idx: int, contract: dict[str, Any], feature: str) -> str:
    """Render a single API conformance test case."""
    endpoint = contract.get("endpoint", f"GET /api/{feature}")
    requires = contract.get("requires", [])
    ensures = contract.get("ensures", [])
    method, _, path = endpoint.partition(" ")
    method = method.upper() if method else "GET"
    path = path.strip() if path else f"/api/{feature}"

    # Build readable description
    desc_parts = []
    if requires:
        desc_parts.append(f"Requires: {'; '.join(str(r) for r in requires[:2])}")
    if ensures:
        desc_parts.append(f"Ensures: {'; '.join(str(e) for e in ensures[:2])}")
    description = " | ".join(desc_parts) or f"Conformance test for {endpoint}"

    fn_name = f"test_api_{feature.replace('-', '_').replace(' ', '_')}_{idx}"

    return textwrap.dedent(f"""\
        def {fn_name}(base_url: str) -> None:
            \"\"\"
            {description}
            Spec: {endpoint}
            \"\"\"
            url = f"{{base_url}}{path}"
            response = httpx.{method.lower()}(url, timeout=10.0)
            # Verify the endpoint is reachable and returns a valid response
            assert response.status_code < 500, (
                f"Server error {{response.status_code}} from {{url}}: {{response.text[:200]}}"
            )
        """)


def _render_e2e_test_case(idx: int, flow: dict[str, Any], feature: str) -> str:
    """Render a single E2E Playwright test case."""
    component = flow.get("component", flow.get("name", f"{feature} component"))
    behavior = flow.get("behavior", flow.get("description", flow.get("claim", "User interaction")))
    # Sanitize: remove newlines and excessive whitespace that break docstring indentation
    component = " ".join(str(component).split())[:200]
    behavior = " ".join(str(behavior).split())[:200]
    steps = flow.get("steps", flow.get("states", []))

    fn_name = f"test_e2e_{feature.replace('-', '_').replace(' ', '_')}_{idx}"
    steps_comment = (
        "\n    #   ".join(str(s) for s in steps[:5]) if steps else "navigate and interact"
    )

    return textwrap.dedent(f"""\
        def {fn_name}(page: Page, base_url: str) -> None:
            \"\"\"
            E2E test: {component}
            Behavior: {behavior}
            \"\"\"
            page.goto(base_url)
            # Steps: {steps_comment}
            # Verify the page loads without critical errors
            expect(page.locator("body")).to_be_visible()
        """)


def _render_auth_test_case(idx: int, scenario: dict[str, Any], feature: str) -> str:
    """Render a single auth scenario test case."""
    scenario_name = scenario.get("scenario", scenario.get("name", f"auth scenario {idx}"))
    method = scenario.get("method", "GET")
    path = scenario.get("path", f"/api/{feature}")
    expected_status = scenario.get("expected_status", scenario.get("unauthenticated_status", 401))

    fn_name = f"test_auth_{feature.replace('-', '_').replace(' ', '_')}_{idx}"

    return textwrap.dedent(f"""\
        def {fn_name}(base_url: str) -> None:
            \"\"\"
            Auth scenario: {scenario_name}
            Verify unauthenticated access is rejected.
            \"\"\"
            url = f"{{base_url}}{path}"
            response = httpx.{method.lower()}(url, timeout=10.0)
            assert response.status_code in ({expected_status}, 401, 403), (
                f"Expected auth rejection from {{url}}, got {{response.status_code}}"
            )
        """)


def _render_schema_test_case(idx: int, model: dict[str, Any], feature: str) -> str:
    """Render a single schema validation test case."""
    entity = model.get("entity", model.get("name", f"{feature} entity"))
    fields = model.get("fields", {})
    constraints = model.get("constraints", [])

    fn_name = f"test_schema_{feature.replace('-', '_').replace(' ', '_')}_{idx}"

    # Build field presence assertions
    field_assertions = ""
    if isinstance(fields, dict) and fields:
        field_list = list(fields.keys())[:5]
        field_assertions = "\n    ".join(
            f'assert {repr(k)} in data or {repr(k)} in data.get("data", {{}}), '
            f'"Missing field {k!r} from {entity}"'
            for k in field_list
        )
    elif isinstance(fields, list) and fields:
        field_assertions = "\n    ".join(
            f'assert {repr(k)} in data or {repr(k)} in data.get("data", {{}}), '
            f'"Missing field {k!r} from {entity}"'
            for k in fields[:5]
        )

    constraint_comment = (
        "\n    # Constraints: " + "; ".join(str(c) for c in constraints[:3])
        if constraints
        else ""
    )

    return textwrap.dedent(f"""\
        def {fn_name}(base_url: str) -> None:
            \"\"\"
            Schema validation: {entity}
            Fields: {list(fields.keys())[:5] if isinstance(fields, dict) else fields[:5]}
            \"\"\"
            url = f"{{base_url}}/api/{feature}"
            response = httpx.get(url, timeout=10.0)
            if response.status_code == 200:
                data = response.json()
                if isinstance(data, list) and data:
                    data = data[0]
                elif isinstance(data, dict) and "results" in data and data["results"]:
                    data = data["results"][0]{constraint_comment}
                {field_assertions if field_assertions else "pass  # schema check: valid JSON"}
        """)


