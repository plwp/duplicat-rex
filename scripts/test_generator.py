"""
TestGenerator — generates conformance test suites from synthesised SpecBundles.

Design:
- API_ENDPOINT specs → pytest + httpx API conformance tests
- USER_FLOW specs    → pytest + playwright E2E tests
- AUTH specs         → pytest + httpx auth scenario tests
- DATA_MODEL specs   → pytest + httpx schema validation tests
- All tests are dual-execution: same file runs against TARGET_URL or CLONE_URL
  via a BASE_URL pytest fixture that reads from env.
- Tolerated variance: IDs, timestamps, and ordering excluded from assertions.
- Output: test files written to output_dir/tests/conformance/

Invariants:
    INV-TG-001: Every generated test file is valid Python (ast.parse passes).
    INV-TG-002: At least one test is generated per SpecItem that has an
                api_contracts or user_flows or auth_scenarios entry.
    INV-TG-003: spec_coverage[feature] == True iff >=1 test generated for that feature.
"""

from __future__ import annotations

import ast
import textwrap
from dataclasses import dataclass
from pathlib import Path
from typing import Any

from scripts.models import SpecBundle, SpecItem
from scripts.spec_store import SpecStore

# ---------------------------------------------------------------------------
# Non-deterministic field names — excluded from conformance assertions
# ---------------------------------------------------------------------------

_VOLATILE_FIELDS = frozenset(
    [
        "id",
        "uuid",
        "created_at",
        "updated_at",
        "modified_at",
        "timestamp",
        "token",
        "session_id",
        "request_id",
        "trace_id",
        "etag",
        "last_modified",
    ]
)

# ---------------------------------------------------------------------------
# Output types
# ---------------------------------------------------------------------------


@dataclass
class GeneratedTestSuite:
    output_dir: Path
    test_files: list[Path]
    total_tests: int
    by_category: dict[str, int]  # api: N, e2e: N, auth: N, schema: N
    spec_coverage: dict[str, bool]  # feature -> has_tests


