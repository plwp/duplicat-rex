# Utilities
# ---------------------------------------------------------------------------


def _feature_slug(feature: str) -> str:
    """Convert a feature key to a safe Python identifier for file/function names."""
    return feature.lower().replace("-", "_").replace(" ", "_").replace(".", "_")


def _validate_python_syntax(path: Path) -> None:
    """Raise SyntaxError if the file is not valid Python (INV-TG-001)."""
    source = path.read_text(encoding="utf-8")
    try:
        ast.parse(source)
    except SyntaxError as exc:
        raise SyntaxError(
            f"Generated file {path} has invalid Python syntax: {exc}"
        ) from exc
