class TestGenerator:
    """
    Generates conformance test suites from synthesised SpecBundles.

    Each SpecItem in the bundle is mapped to one or more generated test files:
    - api_contracts  → tests/conformance/test_api_{feature}.py
    - user_flows     → tests/conformance/test_e2e_{feature}.py
    - auth_scenarios → tests/conformance/test_auth_{feature}.py
    - data_models    → tests/conformance/test_schema_{feature}.py

    All generated tests are dual-execution: set BASE_URL env var to switch
    between target and clone conformance runs.
    """

    def __init__(self, spec_store: SpecStore) -> None:
        self.spec_store = spec_store

    # ------------------------------------------------------------------
    # Public API
    # ------------------------------------------------------------------

    def generate(
        self,
        bundle: SpecBundle,
        *,
        output_dir: Path,
        target_url: str,
        clone_url: str,
    ) -> GeneratedTestSuite:
        """
        Generate a full conformance test suite from a SpecBundle.

        For each SpecItem in the bundle, generate test cases:
        - API_ENDPOINT / api_contracts  → httpx-based API conformance tests
        - USER_FLOW / user_flows        → Playwright E2E test scripts
        - AUTH / auth_scenarios         → auth scenario tests
        - DATA_MODEL / data_models      → schema validation tests

        All tests are dual-execution: same test runs against both target_url
        and clone_url via BASE_URL env var.

        Tolerated variance: IDs, timestamps, ordering excluded from assertions.

        REQUIRES: bundle has at least one SpecItem.
        ENSURES:  returned suite.test_files are all valid Python (ast.parse).
        ENSURES:  spec_coverage[feature] == True iff >=1 test generated.
        """
        conformance_dir = output_dir / "tests" / "conformance"
        conformance_dir.mkdir(parents=True, exist_ok=True)

        test_files: list[Path] = []
        by_category: dict[str, int] = {"api": 0, "e2e": 0, "auth": 0, "schema": 0}
        spec_coverage: dict[str, bool] = {feature: False for feature in bundle.scope}

        for item in bundle.spec_items:
            feature = item.feature
            content = item.content

            # API conformance tests
            api_count = self._generate_api_tests(
                item, content, conformance_dir, test_files
            )
            by_category["api"] += api_count
            if api_count:
                spec_coverage[feature] = True

            # E2E tests
            e2e_count = self._generate_e2e_tests(
                item, content, conformance_dir, test_files
            )
            by_category["e2e"] += e2e_count
            if e2e_count:
                spec_coverage[feature] = True

            # Auth tests
            auth_count = self._generate_auth_tests(
                item, content, conformance_dir, test_files
            )
            by_category["auth"] += auth_count
            if auth_count:
                spec_coverage[feature] = True

            # Schema validation tests
            schema_count = self._generate_schema_tests(
                item, content, conformance_dir, test_files
            )
            by_category["schema"] += schema_count
            if schema_count:
                spec_coverage[feature] = True

        # Validate generated files are syntactically valid Python (INV-TG-001)
        for path in test_files:
            _validate_python_syntax(path)

        total_tests = sum(by_category.values())

        return GeneratedTestSuite(
            output_dir=output_dir,
            test_files=test_files,
            total_tests=total_tests,
            by_category=by_category,
            spec_coverage=spec_coverage,
        )

    # ------------------------------------------------------------------
    # Internal: API test generation
    # ------------------------------------------------------------------

    def _generate_api_tests(
        self,
        item: SpecItem,
        content: dict[str, Any],
        out_dir: Path,
        test_files: list[Path],
    ) -> int:
        """Generate API conformance tests from api_contracts in the spec content."""
        contracts = content.get("api_contracts", [])
        if not contracts:
            return 0

        feature = item.feature
        slug = _feature_slug(feature)
        filename = f"test_api_{slug}.py"
        test_cases_code = []

        for i, contract in enumerate(contracts):
            test_cases_code.append(_render_api_test_case(i, contract, feature))

        test_code = _API_TEST_TEMPLATE.format(
            feature=feature,
            filename=filename,
            test_cases="\n\n".join(test_cases_code),
        )

        out_path = out_dir / filename
        out_path.write_text(test_code, encoding="utf-8")
        test_files.append(out_path)
        return len(contracts)

    # ------------------------------------------------------------------
    # Internal: E2E test generation
    # ------------------------------------------------------------------

    def _generate_e2e_tests(
        self,
        item: SpecItem,
        content: dict[str, Any],
        out_dir: Path,
        test_files: list[Path],
    ) -> int:
        """Generate Playwright E2E tests from user_flows in the spec content."""
        flows = content.get("user_flows", [])
        # Also generate from ui_patterns if user_flows is absent
        if not flows:
            flows = content.get("ui_patterns", [])
        if not flows:
            return 0

        feature = item.feature
        slug = _feature_slug(feature)
        filename = f"test_e2e_{slug}.py"
        test_cases_code = []

        for i, flow in enumerate(flows):
            test_cases_code.append(_render_e2e_test_case(i, flow, feature))

        test_code = _E2E_TEST_TEMPLATE.format(
            feature=feature,
            filename=filename,
            test_cases="\n\n".join(test_cases_code),
        )

        out_path = out_dir / filename
        out_path.write_text(test_code, encoding="utf-8")
        test_files.append(out_path)
        return len(flows)

    # ------------------------------------------------------------------
    # Internal: Auth test generation
    # ------------------------------------------------------------------

    def _generate_auth_tests(
        self,
        item: SpecItem,
        content: dict[str, Any],
        out_dir: Path,
        test_files: list[Path],
    ) -> int:
        """Generate auth scenario tests from auth_scenarios in the spec content."""
        scenarios = content.get("auth_scenarios", [])
        # Also check business_rules for auth-related rules
        if not scenarios and item.spec_type in ("auth_spec", "auth_contract"):
            scenarios = [{"scenario": r} for r in content.get("business_rules", [])]
        if not scenarios:
            return 0

        feature = item.feature
        slug = _feature_slug(feature)
        filename = f"test_auth_{slug}.py"
        test_cases_code = []

        for i, scenario in enumerate(scenarios):
            test_cases_code.append(_render_auth_test_case(i, scenario, feature))

        test_code = _AUTH_TEST_TEMPLATE.format(
            feature=feature,
            filename=filename,
            test_cases="\n\n".join(test_cases_code),
        )

        out_path = out_dir / filename
        out_path.write_text(test_code, encoding="utf-8")
        test_files.append(out_path)
        return len(scenarios)

    # ------------------------------------------------------------------
    # Internal: Schema test generation
    # ------------------------------------------------------------------

    def _generate_schema_tests(
        self,
        item: SpecItem,
        content: dict[str, Any],
        out_dir: Path,
        test_files: list[Path],
    ) -> int:
        """Generate schema validation tests from data_models in the spec content."""
        models = content.get("data_models", [])
        if not models:
            return 0

        feature = item.feature
        slug = _feature_slug(feature)
        filename = f"test_schema_{slug}.py"
        test_cases_code = []

        for i, model in enumerate(models):
            test_cases_code.append(_render_schema_test_case(i, model, feature))

        test_code = _SCHEMA_TEST_TEMPLATE.format(
            feature=feature,
            filename=filename,
            test_cases="\n\n".join(test_cases_code),
        )

        out_path = out_dir / filename
        out_path.write_text(test_code, encoding="utf-8")
        test_files.append(out_path)
        return len(models)


